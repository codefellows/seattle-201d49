// Data Types
// Variables

// String - Words
var str = "John";
console.log(str, typeof str);

// Number - Numbers
var num = 7;
var age = "50";
var actualAge = parseInt(age);
console.log(num, typeof num);
console.log(age, typeof age);
console.log(actualAge, typeof actualAge);

// Boolean - True or False
var raining = false;
console.log(raining, typeof raining);

// Equivalency
console.log(7==7);
console.log(7===7);
console.log(7=="7");
console.log(7==="7");

// Array - List or Collection of stuff
var family = [1,2,3];
console.log(family, typeof family);

// =    Assignment -- set this thing to that value
// ==   Equivalency (soft) value equality
// ===  Equivalency (hard) value and type equality



